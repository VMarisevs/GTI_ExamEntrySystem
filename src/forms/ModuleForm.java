    private void formInternalFrameActivated(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameActivated
        // getting modules list from database
        subjectList = newConnection.getSubjectList(STARTME.TESTING);

        lstBox.removeAllElements();
        for (int i=0; i < subjectList.size(); i++){
            lstBox.addElement(""+subjectList.get(i).getSubjectName());
        }
    }//GEN-LAST:event_formInternalFrameActivated

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // updating fields in database
     
        if (!(txtSubjectID.getText().isEmpty()) &&
                         !(txtModuleName.getText().isEmpty()) &&
                         !(txtDesc.getText().isEmpty()))
                         {



                subjectList.get(lstPersons.getSelectedIndex()).setSubjectName(txtModuleName.getText());
                subjectList.get(lstPersons.getSelectedIndex()).setSubjectDescription(txtDesc.getText());

                newConnection.saveSubject(subjectList.get(lstPersons.getSelectedIndex()), STARTME.TESTING);

                this.setVisible(false);
                eraseInfo(true);
        }
      
    }//GEN-LAST:event_btnSaveActionPerformed

