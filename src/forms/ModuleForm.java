    private void lstPersonsValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstPersonsValueChanged
        // updating screen informaiton about modules
        if (!(lstPersons.isSelectionEmpty())){
            txtSubjectID.setText("" + subjectList.get(lstPersons.getSelectedIndex()).getSubjectID());
            txtModuleName.setText("" + subjectList.get(lstPersons.getSelectedIndex()).getSubjectName());
            txtDesc.setText("" + subjectList.get(lstPersons.getSelectedIndex()).getSubjectDescription());
        }
    }//GEN-LAST:event_lstPersonsValueChanged

